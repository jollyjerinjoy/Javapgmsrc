package firstjava;

public class OperatorExample {

	public static void main(String[] args) {
		int x=3;
		int y=7;
		System.out.println(y-x);
		System.out.println(y+x);
		System.out.println((y/x));
		System.out.println(y/x);
		System.out.println(y*x);
		y++;
		System.out.println(y);
		System.out.println(++y);
		y--;
		System.out.println(y);
		System.out.println(--y);
		int h=5;
		System.out.println(h%2);
	}

}
